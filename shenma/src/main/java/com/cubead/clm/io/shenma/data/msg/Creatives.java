package com.cubead.clm.io.shenma.data.msg;

public class Creatives {
	private Long campaignId;
	private String campaignName;
	private Long adgroupId;
	private String adgroupName;
	private Long creativeId;
	private String title;
	private String description;
	private String destinationUrl;
	private String displayUrl;
	private Boolean pause;
	private Integer status;
	private String negativeReason;
	public Creatives() {

	}
	public Long getCampaignId() {
		return campaignId;
	}
	public void setCampaignId(Long campaignId) {
		this.campaignId = campaignId;
	}
	public String getCampaignName() {
		return campaignName;
	}
	public void setCampaignName(String campaignName) {
		this.campaignName = campaignName;
	}
	public Long getAdgroupId() {
		return adgroupId;
	}
	public void setAdgroupId(Long adgroupId) {
		this.adgroupId = adgroupId;
	}
	public String getAdgroupName() {
		return adgroupName;
	}
	public void setAdgroupName(String adgroupName) {
		this.adgroupName = adgroupName;
	}
	public Long getCreativeId() {
		return creativeId;
	}
	public void setCreativeId(Long creativeId) {
		this.creativeId = creativeId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDestinationUrl() {
		return destinationUrl;
	}
	public void setDestinationUrl(String destinationUrl) {
		this.destinationUrl = destinationUrl;
	}
	public String getDisplayUrl() {
		return displayUrl;
	}
	public void setDisplayUrl(String displayUrl) {
		this.displayUrl = displayUrl;
	}
	public Boolean getPause() {
		return pause;
	}
	public void setPause(Boolean pause) {
		this.pause = pause;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getNegativeReason() {
		return negativeReason;
	}
	public void setNegativeReason(String negativeReason) {
		this.negativeReason = negativeReason;
	}
	
	
	
	
}
